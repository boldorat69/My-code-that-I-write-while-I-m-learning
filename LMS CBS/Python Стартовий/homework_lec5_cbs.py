#Створіть програму, яка зчитує рядок, в якому знаходиться ПІБ користувача і перевіряє,
#чи складається рядок з літер, при чому кожне слово має бути записане з великої літери. 
#Вивести результат на екран.
full_name = input("Введіть ПІБ: ").strip()
# Перевірка, що всі символи — літери або пробіли
if all(ch.isalpha() or ch.isspace() for ch in full_name):
    # Розділити на слова
    words = full_name.split()
    # Перевірити, що кожне слово з великої літери
    if all(word.istitle() for word in words):
        print("Рядок правильний!")
    else:
        print("Кожне слово має починатися з великої літери.")
else:
    print("Рядок повинен містити лише літери та пробіли.")
