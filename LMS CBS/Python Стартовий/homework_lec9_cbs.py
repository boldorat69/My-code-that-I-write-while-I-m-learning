# Нехай на кожну сходинку можна стати з попередньої або переступивши через одну.
# Визначте, скількома способами можна піднятися на задану сходинку.
def ways(n):
    if n <= 0:
        return 0
    elif n == 1:
        return 1
    elif n == 2:
        return 2
    else:
        a, b = 1, 2
        for _ in range(3, n + 1):
            a, b = b, a + b
        return b


steps = int(input("Введіть номер сходинки: "))
print(f"Кількість способів піднятися: {ways(steps)}")


