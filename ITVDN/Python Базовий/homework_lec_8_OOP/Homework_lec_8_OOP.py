import time
import winsound

def countdown(seconds):
    print(f"Countdown started for {seconds} seconds...")
    while seconds > 0:
        mins, secs = divmod(seconds, 60)
        timer = '{:02d}:{:02d}'.format(mins, secs)
        print(timer)
        time.sleep(1)
        seconds -= 1
    
    # Звуковий сигнал (частота 1000 Гц, тривалість 1000 мс)
    winsound.Beep(1000, 1000)
    print("Time's up!")

my_timer = countdown(7)

